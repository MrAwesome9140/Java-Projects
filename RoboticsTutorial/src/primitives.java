public class primitives {

    void bytes(){
        byte b = 12;
        byte c = -12;
    }

    void shorts() {
        short p = 5;
        short t = -20;
        short y = 423;
    }

    void ints() {
        int i = 0;
        int z = -10;
        int k = 9345;
    }

    void longs(){
        long a = 104930483;
        long u = 934587394;
        long t = 938438979;
    }

    void floats(){
        float f = 5.0f;
        float d = 6.598f;
    }

    void doubles(){
        double d = 30498.234802;
        double t = 23842348.23472937892387293;
        double happy = 5.6;
        double i = -9.6;
    }

    void booleans(){
        boolean c = true;
        c = false;
        c = !c;
    }

    void chars(){
        char c = 'r';
        char d = '?';
        char t = '\'';
    }

}
